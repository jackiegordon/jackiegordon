package Homework;

public class BreakDemo {

	public static void main(String[] args) {
		
		for (int i =0; i<= 15; i++) {
		
			if (i==7) {
				System.out.println("Dania's favorite Number: " +i);
				break;
			}
		System.out.println(i);
		
		
	}
}
}
